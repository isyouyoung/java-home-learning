package variable.ex08.ex07_Car;

public class Driver {

  private String name;
  private int career;

  public void setName(String name) {
    this.name = name;
  }

  public void setCareer(int career) {
    this.career = career;
  }

  public String getName() {
    return this.name;
  }

  public int getCareer() {
    return this.career;
  }

}
